#!/usr/bin/env bash
# setup.sh — Set up or update Claude Code agents/skills in any project
#
# Usage: ./setup.sh [target-directory]                          # Auto-detects setup vs update
#        ./setup.sh --update [target-directory]                 # Force update mode
#        ./setup.sh --compatible opencode,gemini [target-dir]   # Generate native agent files for other agents
#
# One-liner (run from your project directory):
#   bash <(curl -fsSL https://raw.githubusercontent.com/nickmaglowsch/claude-setup/main/setup.sh)
#   bash <(curl -fsSL https://raw.githubusercontent.com/nickmaglowsch/claude-setup/main/setup.sh) --compatible opencode,gemini

set -euo pipefail

REPO_URL="https://github.com/nickmaglowsch/claude-setup.git"
CLONE_DIR="/tmp/claude-setup"

# --- Bootstrap: ensure we're running from a full repo clone ---
# When piped via curl or run standalone without the repo files around it,
# clone the repo to /tmp and re-exec from there.
_self_bootstrap() {
  # Check if the template files exist next to this script
  local dir
  dir="$(cd "$(dirname "${BASH_SOURCE[0]:-$0}")" 2>/dev/null && pwd)" || dir=""

  # If .claude/agents/ exists next to us, we're running from the repo — nothing to do
  if [ -n "$dir" ] && [ -d "$dir/.claude/agents" ]; then
    SCRIPT_DIR="$dir"
    return
  fi

  # We're running standalone (curl pipe, downloaded copy, etc.) — need the full repo
  echo "=== Fetching claude-setup template ==="

  if [ -d "$CLONE_DIR/.git" ] && git -C "$CLONE_DIR" rev-parse --git-dir &>/dev/null; then
    echo "  Found existing clone at $CLONE_DIR, pulling latest..."
    git -C "$CLONE_DIR" pull --ff-only 2>&1 | sed 's/^/  /'
  else
    echo "  Cloning $REPO_URL to $CLONE_DIR..."
    rm -rf "$CLONE_DIR"
    git clone --depth 1 "$REPO_URL" "$CLONE_DIR" 2>&1 | sed 's/^/  /'
  fi

  echo ""

  # Re-exec from the cloned repo, passing all original args
  exec "$CLONE_DIR/setup.sh" "$@"
}

_self_bootstrap "$@"

# --- Parse flags ---
FORCE_UPDATE=false
TARGET_DIR=""
COMPATIBLE_AGENTS=()
ADD_DEVCONTAINER=false
GLOBAL_MODE=false

while [[ $# -gt 0 ]]; do
  case $1 in
    --update|-u)
      FORCE_UPDATE=true
      shift
      ;;
    --compatible|-c)
      IFS=',' read -ra COMPATIBLE_AGENTS <<< "$2"
      shift 2
      ;;
    --add-devcontainer)
      ADD_DEVCONTAINER=true
      shift
      ;;
    --global|-g)
      GLOBAL_MODE=true
      shift
      ;;
    -h|--help)
      echo "Usage: $0 [--update] [--compatible <agents>] [--global] [target-directory]"
      echo ""
      echo "  target-directory          Project to set up (default: current directory)"
      echo "  --update, -u              Force update mode (skip auto-detection)"
      echo "  --compatible, -c <list>   Comma-separated list of agents to generate native files for"
      echo "                            Supported: opencode, gemini"
      echo "                            Example: --compatible opencode,gemini"
      echo "  --add-devcontainer        Add devcontainer support (update mode only)"
      echo "  --global, -g              Install agents and skills into ~/.claude/ (global, not per-project)"
      echo ""
      echo "  What gets generated by --compatible:"
      echo "    opencode  →  .opencode/agents/*.md  +  AGENTS.md -> CLAUDE.md symlink"
      echo "    gemini    →  .gemini/agents/*.toml  +  GEMINI.md -> CLAUDE.md symlink"
      echo ""
      echo "  You will be prompted to choose models (heavy/standard tier) for each agent system."
      echo ""
      echo "One-liner (run from your project directory):"
      echo "  bash <(curl -fsSL https://raw.githubusercontent.com/nickmaglowsch/claude-setup/main/setup.sh)"
      exit 0
      ;;
    *)
      TARGET_DIR="$1"
      shift
      ;;
  esac
done

# Default to current directory if no target specified
if [ -z "$TARGET_DIR" ]; then
  TARGET_DIR="$(pwd)"
fi

if [ ! -d "$TARGET_DIR" ]; then
  echo "Error: '$TARGET_DIR' does not exist or is not a directory" >&2
  exit 1
fi

TARGET_DIR="$(cd "$TARGET_DIR" && pwd)"

