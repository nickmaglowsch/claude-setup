# --- Auto-update interval config ---
AUTO_UPDATE_CONF="$HOME/.claude/auto-update.conf"

# Convert a human-friendly interval (5m, 30m, 1h, 6h, 1d) to a cron schedule.
_interval_to_cron() {
  local interval="$1"
  local num="${interval%[mhdMHD]}"
  local unit="${interval##*[0-9]}"
  unit=$(echo "$unit" | tr '[:upper:]' '[:lower:]')

  if ! [[ "$num" =~ ^[0-9]+$ ]] || [ "$num" -lt 1 ]; then
    echo ""
    return
  fi

  case "$unit" in
    m)
      if [ "$num" -ge 60 ]; then
        echo ""
      else
        echo "*/$num * * * *"
      fi
      ;;
    h)
      if [ "$num" -ge 24 ]; then
        echo ""
      elif [ "$num" -eq 1 ]; then
        echo "0 * * * *"
      else
        echo "0 */$num * * *"
      fi
      ;;
    d)
      # cron "*/N" for days-of-month resets at month boundaries, so only "1d" is reliable
      if [ "$num" -eq 1 ]; then
        echo "0 0 * * *"
      else
        echo ""
      fi
      ;;
    *)
      echo ""
      ;;
  esac
}

# Human-readable label for an interval value.
_interval_label() {
  local interval="$1"
  local num="${interval%[mhdMHD]}"
  local unit="${interval##*[0-9]}"
  unit=$(echo "$unit" | tr '[:upper:]' '[:lower:]')

  case "$unit" in
    m)
      if [ "$num" -eq 1 ]; then echo "every minute"
      else echo "every ${num} minutes"; fi ;;
    h)
      if [ "$num" -eq 1 ]; then echo "every hour"
      else echo "every ${num} hours"; fi ;;
    d) echo "daily" ;;
    *) echo "$interval" ;;
  esac
}

# Read the persisted interval, defaulting to 5m.
_get_auto_update_interval() {
  if [ -n "$AUTO_UPDATE_INTERVAL" ]; then
    echo "$AUTO_UPDATE_INTERVAL"
  elif [ -f "$AUTO_UPDATE_CONF" ]; then
    local saved
    saved=$(grep -E '^interval=' "$AUTO_UPDATE_CONF" 2>/dev/null | head -1 | cut -d= -f2)
    echo "${saved:-5m}"
  else
    echo "5m"
  fi
}

# Persist the interval to the config file.
_save_auto_update_interval() {
  local interval="$1"
  mkdir -p "$(dirname "$AUTO_UPDATE_CONF")"
  echo "interval=$interval" > "$AUTO_UPDATE_CONF"
}

# --- Auto-update: install/refresh cron job ---
_install_auto_update() {
  local script="$HOME/.claude/auto-update.sh"
  local interval
  interval=$(_get_auto_update_interval)
  local cron_schedule
  cron_schedule=$(_interval_to_cron "$interval")

  if [ -z "$cron_schedule" ]; then
    echo "  Error: invalid interval '$interval'. Use examples like 5m, 30m, 1h, 6h, 1d." >&2
    return 1
  fi

  local cron_entry="$cron_schedule bash \"$script\" >> \"$HOME/.claude/auto-update.log\" 2>&1"

  cp "$SCRIPT_DIR/auto-update.sh" "$script"
  chmod +x "$script"

  # Remove any existing auto-update cron entry before adding the new one
  local existing_crontab
  existing_crontab=$(crontab -l 2>/dev/null | grep -vF "auto-update.sh" || true)
  if [ -n "$existing_crontab" ]; then
    (echo "$existing_crontab"; echo "$cron_entry") | crontab -
  else
    echo "$cron_entry" | crontab -
  fi

  _save_auto_update_interval "$interval"
  echo "  Installed cron job: checks $(_interval_label "$interval")."
  # WSL: cron may need to be started manually
  if grep -qi microsoft /proc/version 2>/dev/null; then
    echo "  Note (WSL): run 'sudo service cron start' if cron is not already running."
    echo "  To start cron automatically in WSL, add it to /etc/wsl.conf or your shell profile."
  fi
}

