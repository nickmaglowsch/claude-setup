_install_rtk() {
  local os
  os="$(uname -s)"

  # macOS: prefer brew, fallback to curl install script
  if [ "$os" = "Darwin" ]; then
    if command -v brew &>/dev/null; then
      echo "  Installing RTK via Homebrew..."
      if brew install rtk 2>&1 | sed 's/^/    /'; then
        return 0
      fi
      echo "  Homebrew install failed, trying curl installer..."
    fi
  fi

  # Linux/WSL/macOS fallback: curl install script
  echo "  Installing RTK via install script..."
  if curl -fsSL https://raw.githubusercontent.com/rtk-ai/rtk/refs/heads/master/install.sh | sh 2>&1 | sed 's/^/    /'; then
    # Ensure ~/.local/bin is in PATH for current session
    if ! command -v rtk &>/dev/null; then
      export PATH="$HOME/.local/bin:$PATH"
    fi
    if command -v rtk &>/dev/null; then
      # Check if ~/.local/bin is already in shell profile
      local shell_rc="$HOME/.bashrc"
      if [ -n "${ZSH_VERSION:-}" ] || [ "$(basename "${SHELL:-}")" = "zsh" ]; then
        shell_rc="$HOME/.zshrc"
      fi
      if ! grep -q '\.local/bin' "$shell_rc" 2>/dev/null; then
        echo ""
        echo "  Note: add this to $shell_rc for permanent PATH access:"
        echo "    export PATH=\"\$HOME/.local/bin:\$PATH\""
      fi
      return 0
    fi
  fi

  echo "  Could not install RTK automatically."
  echo "  Install manually: https://github.com/rtk-ai/rtk#installation"
  return 1
}

_merge_global_deny_rules() {
  local settings_file="$HOME/.claude/settings.json"

  # Fast-path: already merged
  if [ -f "$settings_file" ] && grep -q 'Read(\*\*/node_modules/\*\*)' "$settings_file" 2>/dev/null; then
    echo "  Global deny rules already present — skipping."
    return 0
  fi

  mkdir -p "$HOME/.claude"

  # Build JSON array of deny rules
  local rules_json="["
  local first=true
  for rule in "${GLOBAL_DENY_RULES[@]}"; do
    if [ "$first" = true ]; then
      first=false
    else
      rules_json+=","
    fi
    rules_json+="\"$rule\""
  done
  rules_json+="]"

  if [ ! -f "$settings_file" ]; then
    # No existing file — create from scratch
    python3 -c "
import json, sys
rules = json.loads(sys.argv[1])
data = {'permissions': {'deny': rules}}
print(json.dumps(data, indent=2))
" "$rules_json" > "$settings_file" 2>/dev/null && {
      echo "  Created ~/.claude/settings.json with global deny rules."
      return 0
    }
    # python3 failed, try jq
    if command -v jq &>/dev/null; then
      echo "$rules_json" | jq '{permissions: {deny: .}}' > "$settings_file" 2>/dev/null && {
        echo "  Created ~/.claude/settings.json with global deny rules."
        return 0
      }
    fi
  else
    # Merge into existing file
    local merged
    merged=$(python3 -c "
import json, sys
rules = json.loads(sys.argv[1])
with open(sys.argv[2]) as f:
    data = json.load(f)
perms = data.setdefault('permissions', {})
existing = set(perms.get('deny', []))
perms['deny'] = perms.get('deny', []) + [r for r in rules if r not in existing]
print(json.dumps(data, indent=2))
" "$rules_json" "$settings_file" 2>/dev/null) && {
      echo "$merged" > "$settings_file"
      echo "  Merged global deny rules into ~/.claude/settings.json."
      return 0
    }
    # python3 failed, try jq
    if command -v jq &>/dev/null; then
      merged=$(jq --argjson rules "$rules_json" '
        .permissions //= {} |
        .permissions.deny //= [] |
        .permissions.deny = (.permissions.deny + ($rules - .permissions.deny))
      ' "$settings_file" 2>/dev/null) && {
        echo "$merged" > "$settings_file"
        echo "  Merged global deny rules into ~/.claude/settings.json."
        return 0
      }
    fi
  fi

  echo "  Warning: Could not merge deny rules (python3 and jq unavailable)."
  echo "  Add these rules manually to ~/.claude/settings.json:"
  echo "  https://github.com/nickmaglowsch/claude-setup#token-reducer-pack"
  return 1
}

_install_context_mode() {
  local claude_json="$HOME/.claude.json"

  # Check if already configured (claude mcp add writes to ~/.claude.json)
  if { [ -f "$claude_json" ] && grep -q '"context-mode"' "$claude_json" 2>/dev/null; } || \
     { [ -f "$HOME/.claude/settings.json" ] && grep -q '"context-mode"' "$HOME/.claude/settings.json" 2>/dev/null; }; then
    echo "  context-mode MCP server already configured — skipping."
    return 0
  fi

  # Check for npx — resolve full path so MCP startup works even if
  # Node is managed by mise/nvm/fnm and not on the default PATH.
  local npx_path
  npx_path=$(command -v npx 2>/dev/null) || {
    echo "  npx not found — install Node.js 18+ to use context-mode."
    echo "  Then add manually: https://github.com/mksglu/context-mode#installation"
    return 1
  }

  # Prefer `claude mcp add` (writes to the correct config location)
  if command -v claude &>/dev/null; then
    claude mcp add -s user context-mode -- "$npx_path" -y context-mode@latest 2>/dev/null && {
      echo "  Added context-mode MCP server via 'claude mcp add'."
      return 0
    }
  fi

  # Fallback: merge into ~/.claude.json directly
  local mcp_config="{\"type\":\"stdio\",\"command\":\"$npx_path\",\"args\":[\"-y\",\"context-mode@latest\"],\"env\":{}}"

  if [ ! -f "$claude_json" ]; then
    python3 -c "
import json, sys
config = json.loads(sys.argv[1])
data = {'mcpServers': {'context-mode': config}}
print(json.dumps(data, indent=2))
" "$mcp_config" > "$claude_json" 2>/dev/null && {
      echo "  Created ~/.claude.json with context-mode MCP server."
      return 0
    }
    if command -v jq &>/dev/null; then
      echo "$mcp_config" | jq '{mcpServers: {"context-mode": .}}' > "$claude_json" 2>/dev/null && {
        echo "  Created ~/.claude.json with context-mode MCP server."
        return 0
      }
    fi
  else
    local merged
    merged=$(python3 -c "
import json, sys
config = json.loads(sys.argv[1])
with open(sys.argv[2]) as f:
    data = json.load(f)
mcp = data.setdefault('mcpServers', {})
mcp['context-mode'] = config
print(json.dumps(data, indent=2))
" "$mcp_config" "$claude_json" 2>/dev/null) && {
      echo "$merged" > "$claude_json"
      echo "  Added context-mode MCP server to ~/.claude.json."
      return 0
    }
    if command -v jq &>/dev/null; then
      merged=$(jq --argjson config "$mcp_config" '
        .mcpServers //= {} |
        .mcpServers["context-mode"] = $config
      ' "$claude_json" 2>/dev/null) && {
        echo "$merged" > "$claude_json"
        echo "  Added context-mode MCP server to ~/.claude.json."
        return 0
      }
    fi
  fi

  echo "  Warning: Could not configure context-mode (claude CLI, python3, and jq unavailable)."
  echo "  Add manually: https://github.com/mksglu/context-mode#installation"
  return 1
}

_install_nudge_hook() {
  local hook_src="$SCRIPT_DIR/.claude/hooks/token-reducer-nudge.sh"
  local hook_dest="$HOME/.claude/hooks/token-reducer-nudge.sh"
  local settings_file="$HOME/.claude/settings.json"

  # Copy the nudge script
  mkdir -p "$HOME/.claude/hooks"
  if [ -f "$hook_src" ]; then
    cp "$hook_src" "$hook_dest"
    chmod +x "$hook_dest"
  fi

  # Register the UserPromptSubmit hook in settings.json
  if [ -f "$settings_file" ] && grep -q 'token-reducer-nudge' "$settings_file" 2>/dev/null; then
    return 0  # already registered
  fi

  mkdir -p "$HOME/.claude"

  if [ ! -f "$settings_file" ]; then
    # Create settings.json with just the hook
    cat > "$settings_file" <<'HOOK_EOF'
{
  "hooks": {
    "UserPromptSubmit": [
      {
        "matcher": "",
        "hooks": [
          {
            "type": "command",
            "command": "bash ~/.claude/hooks/token-reducer-nudge.sh"
          }
        ]
      }
    ]
  }
}
HOOK_EOF
  else
    # Merge hook into existing settings.json
    local merged
    merged=$(python3 -c "
import json, sys
with open(sys.argv[1]) as f:
    data = json.load(f)
hooks = data.setdefault('hooks', {})
ups = hooks.setdefault('UserPromptSubmit', [])
# Check if already registered
for entry in ups:
    for h in entry.get('hooks', []):
        if 'token-reducer-nudge' in h.get('command', ''):
            print(json.dumps(data, indent=2))
            sys.exit(0)
ups.append({
    'matcher': '',
    'hooks': [{'type': 'command', 'command': 'bash ~/.claude/hooks/token-reducer-nudge.sh'}]
})
print(json.dumps(data, indent=2))
" "$settings_file" 2>/dev/null) && {
      echo "$merged" > "$settings_file"
      return 0
    }
    # python3 failed, try jq
    if command -v jq &>/dev/null; then
      merged=$(jq '
        .hooks //= {} |
        .hooks.UserPromptSubmit //= [] |
        if (.hooks.UserPromptSubmit | map(.hooks[]?.command // "" | test("token-reducer-nudge")) | any)
        then .
        else .hooks.UserPromptSubmit += [{"matcher": "", "hooks": [{"type": "command", "command": "bash ~/.claude/hooks/token-reducer-nudge.sh"}]}]
        end
      ' "$settings_file" 2>/dev/null) && {
        echo "$merged" > "$settings_file"
        return 0
      }
    fi
  fi
}

_remove_nudge_hook() {
