    chmod +x "$TARGET_DIR/run-claude.sh" 2>/dev/null || true
    echo ""
  fi
fi

# --- Step 3b: Playwright MCP (optional) ---
SETTINGS_JSON="$TARGET_DIR/.claude/settings.json"
