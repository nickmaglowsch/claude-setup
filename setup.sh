# ===========================================================================
# UPDATE MODE
# ===========================================================================
if [ "$UPDATE_MODE" = true ]; then
  # Verify target already has a Claude setup (relevant when --update is forced)
  if [ ! -d "$TARGET_DIR/.claude/agents" ]; then
    echo "Error: '$TARGET_DIR' does not have an existing Claude setup (.claude/agents/ not found)." >&2
    echo "Run without --update for first-time setup." >&2
    exit 1
  fi

  echo "Updating Claude Code setup in: $TARGET_DIR"
  echo ""

  # Pull latest template if this is a git repo
  if git -C "$SCRIPT_DIR" rev-parse --git-dir &>/dev/null; then
    echo "=== Pulling latest template ==="
    git -C "$SCRIPT_DIR" pull --ff-only 2>&1 | sed 's/^/  /'
    echo ""
  else
    echo "Warning: Template directory is not a git repo, using files as-is."
    echo ""
  fi

  # Update core .claude/ files (skip settings.local.json to preserve user config)
  echo "=== Updating: .claude/ agents, skills ==="
  echo ""

  for file in "${CLAUDE_FILES[@]}"; do
    if [ -f "$SCRIPT_DIR/$file" ]; then
      force_copy_file "$SCRIPT_DIR/$file" "$TARGET_DIR/$file"
    fi
  done

  echo ""

  # Update devcontainer if it already exists, or install it if --add-devcontainer was passed
  if [ -d "$TARGET_DIR/.devcontainer" ]; then
    echo "=== Updating: .devcontainer/ ==="
    echo ""

    for file in "${DEVCONTAINER_FILES[@]}"; do
      if [ -f "$SCRIPT_DIR/$file" ]; then
        force_copy_file "$SCRIPT_DIR/$file" "$TARGET_DIR/$file"
      fi
    done

    chmod +x "$TARGET_DIR/.devcontainer/init-firewall.sh" "$TARGET_DIR/.devcontainer/fix-git-worktree.sh" 2>/dev/null || true
    echo ""
  elif [ "$ADD_DEVCONTAINER" = true ]; then
    echo "=== Adding: .devcontainer/ ==="
    echo ""

    for file in "${DEVCONTAINER_FILES[@]}"; do
      if [ -f "$SCRIPT_DIR/$file" ]; then
        force_copy_file "$SCRIPT_DIR/$file" "$TARGET_DIR/$file"
      fi
    done

    chmod +x "$TARGET_DIR/.devcontainer/init-firewall.sh" "$TARGET_DIR/.devcontainer/fix-git-worktree.sh" 2>/dev/null || true
    echo ""
  fi

  # Update run-claude.sh if it already exists in target
  if [ -f "$TARGET_DIR/run-claude.sh" ]; then
    echo "=== Updating: run-claude.sh ==="
    echo ""
    force_copy_file "$SCRIPT_DIR/run-claude.sh" "$TARGET_DIR/run-claude.sh"
    chmod +x "$TARGET_DIR/run-claude.sh" 2>/dev/null || true
    echo ""
  fi

  # Configure Playwright MCP if qa-agent is installed and not already configured
  SETTINGS_JSON="$TARGET_DIR/.claude/settings.json"
  if [ -f "$SETTINGS_JSON" ] && grep -q '"playwright"' "$SETTINGS_JSON" 2>/dev/null; then
    echo "=== Playwright MCP already configured — skipping ==="
    echo ""
  elif [ ! -f "$SETTINGS_JSON" ]; then
    echo "=== Playwright MCP (for /qa browser testing) ==="
    echo ""
    read -rp "  Add Playwright MCP for browser-based QA (/qa skill)? [y/N] " install_playwright
    echo ""
    if [[ "$install_playwright" =~ ^[Yy]$ ]]; then
      mkdir -p "$TARGET_DIR/.claude"
      cat > "$SETTINGS_JSON" <<'MCP_EOF'
{
  "mcpServers": {
    "playwright": {
      "command": "npx",
      "args": ["@playwright/mcp@latest"]
    }
  }
}
MCP_EOF
      echo "  Created: .claude/settings.json (Playwright MCP configured)"
      echo ""
    fi
  fi

  # Ensure .gitignore has required entries
  echo "=== Updating .gitignore ==="
  echo ""

  GITIGNORE_ENTRIES=(
    ".devcontainer/.env"
    ".claude/settings.local.json"
    ".claude/auth.local.md"
    ".claude-worktrees/"
    ".DS_Store"
    "tasks/"
  )

  GITIGNORE_FILE="$TARGET_DIR/.gitignore"
  touch "$GITIGNORE_FILE"

  # Ensure file ends with a newline before appending
  if [ -s "$GITIGNORE_FILE" ] && [ "$(tail -c1 "$GITIGNORE_FILE" | wc -l)" -eq 0 ]; then
    echo "" >> "$GITIGNORE_FILE"
  fi

  # Add section comment if any entries are missing and comment doesn't exist yet
  NEEDS_SECTION=false
  for entry in "${GITIGNORE_ENTRIES[@]}"; do
    if ! grep -qxF "$entry" "$GITIGNORE_FILE" 2>/dev/null; then
      NEEDS_SECTION=true
      break
    fi
  done
  if [ "$NEEDS_SECTION" = true ] && ! grep -qxF "# .claude" "$GITIGNORE_FILE" 2>/dev/null; then
    echo "# .claude" >> "$GITIGNORE_FILE"
  fi

  for entry in "${GITIGNORE_ENTRIES[@]}"; do
    if ! grep -qxF "$entry" "$GITIGNORE_FILE" 2>/dev/null; then
      echo "$entry" >> "$GITIGNORE_FILE"
      echo "  Added to .gitignore: $entry"
    fi
  done

  echo ""

  setup_compat_layer

  echo "=== Update complete! ==="
  echo ""
  echo "  Updated template files. Your settings.local.json and agent-memory/ were preserved."
  echo "  Review the changes with: cd $TARGET_DIR && git diff"
  echo ""
  exit 0
fi

